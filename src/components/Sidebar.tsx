const previewNav = [
  { to: '/secure', label: 'Secure by design', icon: SecureIcon },
  { to: '/observe', label: 'Observe', icon: ObserveIcon },
  { to: '/endpoint', label: 'Endpoint', icon: EndpointIcon },
];

export function Sidebar() {
  const [collapsed, setCollapsed] = useState(false);

  return (
    <nav className={`sidebar${collapsed ? ' sidebar--collapsed' : ''}`}>
      {/* Logo */}
      <div className="sidebar__logo">
        {!collapsed ? (
          <>
            <div className="sidebar__logo-full"><EvoLogo /></div>
            <button className="sidebar__collapse" onClick={() => setCollapsed(true)} aria-label="Collapse sidebar">
              <SidePanelIcon />
            </button>
          </>
        ) : (
          <>
            <div className="sidebar__logo-icon"><EvoLogoIcon /></div>
            <button className="sidebar__collapse sidebar__collapse--expand" onClick={() => setCollapsed(false)} aria-label="Expand sidebar">
              <ExpandIcon />
            </button>
          </>
        )}
      </div>

      {/* Nav */}
      <div className="sidebar__nav">
        {/* Main navigation */}
        <div className="sidebar__section">
          {mainNav.map(({ to, label, icon: Icon, badge }) => (
            <NavLink
              key={to}
              to={to}
              end={to === '/'}
              className={({ isActive }) =>
                `sidebar__item${isActive ? ' sidebar__item--active' : ''}`
              }
            >
              <span className="sidebar__icon"><Icon /></span>
              <span className="sidebar__label">{label}</span>
              {badge && <span className="sidebar__badge">{badge}</span>}
            </NavLink>
          ))}
        </div>

        {/* Preview section */}
        <div className="sidebar__section">
          <span className="sidebar__section-label">Preview</span>
          {previewNav.map(({ to, label, icon: Icon }) => (
            <NavLink
              key={to}
              to={to}
              className={({ isActive }) =>
                `sidebar__item${isActive ? ' sidebar__item--active' : ''}`
              }
            >
              <span className="sidebar__icon"><Icon /></span>
              <span className="sidebar__label">{label}</span>
            </NavLink>
          ))}
        </div>
      </div>
    </nav>
  );
}

